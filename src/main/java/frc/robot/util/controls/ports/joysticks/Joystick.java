package frc.robot.util.controls.ports.joysticks;

/**
 * Human readable variables for every numerical port on a joystick.
 */
public abstract class Joystick {

  /** The button port for when a joystick is pressed down. */
  public static int stick;

  /** Port number for the x-axis of the joystick. */
  public static int xAxis;

  /** Port number for the y-axis of the joystick. */
  public static int yAxis;
}
